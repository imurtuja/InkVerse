import mongoose from "mongoose";

const PendingUserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true,
      lowercase: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
    otp: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  {
    timestamps: true,
  }
);

// Ensure index exists for expiration
PendingUserSchema.index({ createdAt: 1 }, { expireAfterSeconds: 600 });

export default mongoose.models.PendingUser || mongoose.model("PendingUser", PendingUserSchema);
